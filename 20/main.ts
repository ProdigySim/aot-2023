type ToAsciiArt<S extends string> = Decode<ToMapped<Uppercase<S>>>;

type ToMapped<S extends string> = 
  S extends `${infer C extends keyof Letters}${infer D extends keyof Letters}${infer U}` 
    ? [Letters[C], Letters[D], ...ToMapped<U>] 
    : S extends keyof Letters ? [Letters[S]] : [];

type Decode<
  Instr extends Array<[string,string,string] | '\n'>,
  Acc extends [string,string,string] = ['','','']
> =
  Instr extends ['\n', ...infer Rest extends Array<[string,string,string] | '\n'>]
    ?  [...Acc, ...Decode<Rest>]
    : Instr extends [infer U extends [string, string, string], ...infer Rest extends Array<[string,string,string] | '\n'>]
      ? Decode<Rest, [
        `${Acc[0]}${U[0]}`,
        `${Acc[1]}${U[1]}`,
        `${Acc[2]}${U[2]}`,
      ]>
      : Acc;

